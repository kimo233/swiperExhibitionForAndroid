package com.example.backend.model;

public class Program {
    private int programid;
    private String programname;
    private String programresolvingratio;
    private String programcontent;

    public int getProgramid() {
        return programid;
    }

    public void setProgramid(int programid) {
        this.programid = programid;
    }

    public String getProgramname() {
        return programname;
    }

    public void setProgramname(String programname) {
        this.programname = programname;
    }

    public String getProgramresolvingratio() {
        return programresolvingratio;
    }

    public void setProgramresolvingratio(String programresolvingratio) {
        this.programresolvingratio = programresolvingratio;
    }

    public String getProgramcontent() {
        return programcontent;
    }

    public void setProgramcontent(String programcontent) {
        this.programcontent = programcontent;
    }
}
